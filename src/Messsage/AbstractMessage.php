namespace App\Messsage;

use Psr\Log\LoggerInterface;

/**
 * Base Messsage.
 */
abstract class AbstractMessage
{
    /** @var LoggerInterface */
    private $logger;

    /**
     * Class Constructor.
     */
    public function __construct(LoggerInterface $logger)
    {
        $this->logger = $logger;
    }
}
